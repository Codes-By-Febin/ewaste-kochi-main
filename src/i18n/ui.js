/**
 * Utility to get a translation string safely.
 * @param {string} lang The current route language
 * @param {string} key The translation key
 * @returns {string} The translated string or default English fallback
 */
export function useTranslations(lang = defaultLang) {
  return function t(key) {
    if (i18nUI[lang] && i18nUI[lang][key]) {
      return i18nUI[lang][key];
    }
    return i18nUI[defaultLang][key] || key;
  }
}
